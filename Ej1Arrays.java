package ejercicios;

public class Ej1Arrays {

	public static void main(String[] args) {
		
        int numeros[] = new int[5];
        
		char caracteres[] = new char[10];
		
		boolean buleanos[] = new boolean[4];
		
		String nombres[] = new String[6];
		
		
		System.out.println("numeros[0]:"+numeros[0]);
		
		System.out.println("caracteres[9]:"+caracteres[9]);
		
		System.out.println("buleanos[2]:"+buleanos[2]);
		
		System.out.println("nombres[4]"+nombres[4]);
		
		int numeros2[] = {4,6,8,2};
		char caracteres2[] = {'h','o','l','a'};
		boolean buleanos2[] = {true, false, true, true, false};
		String nombres2[] = {"Alex", "Manu", "Alberto"};
		
		System.out.println("numeros2[0]:"+numeros2[0]);
		
		System.out.println("caracteres2[9]:"+caracteres2[3]);
		
		System.out.println("buleanos2[2]:"+buleanos2[2]);
		
		System.out.println("nombres2[4]"+nombres2[2]);
		
		
	}
}
	